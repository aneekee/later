#!/usr/bin/env node
'use strict';

/**
 * Reads messages, message_resolutions, and chats export files,
 * groups create/resolve events by (user_id, day), and generates
 * SQL INSERT statements for the message_burndown_snapshots table.
 *
 * Usage:
 *   node scripts/message-burndown-snapshots/generate-burndown-snapshots.js \
 *     --messages  scripts/message-burndown-snapshots/messages_202605302048.json \
 *     --resolutions scripts/message-burndown-snapshots/message_resolutions_202605302049.json \
 *     --chats     scripts/message-burndown-snapshots/chats_202605302103.json
 */

const fs = require('fs');
const path = require('path');
const { randomUUID } = require('crypto');

// ---------------------------------------------------------------------------
// CLI args
// ---------------------------------------------------------------------------
const args = process.argv.slice(2);
const get = (flag) => {
  const idx = args.indexOf(flag);
  return idx !== -1 ? args[idx + 1] : null;
};

const messagesFile =
  get('--messages') ??
  'scripts/message-burndown-snapshots/messages_202605302048.json';
const resolutionsFile =
  get('--resolutions') ??
  'scripts/message-burndown-snapshots/message_resolutions_202605302049.json';
const chatsFile =
  get('--chats') ??
  'scripts/message-burndown-snapshots/chats_202605302103.json';

// ---------------------------------------------------------------------------
// Load data
// ---------------------------------------------------------------------------
function loadRows(filePath) {
  const abs = path.resolve(filePath);
  const raw = JSON.parse(fs.readFileSync(abs, 'utf8'));
  // The export wraps the array under a single top-level key (the SQL query string)
  return raw[Object.keys(raw)[0]];
}

const messages = loadRows(messagesFile);
const resolutions = loadRows(resolutionsFile);
const chats = loadRows(chatsFile);

// ---------------------------------------------------------------------------
// Build lookup maps
// ---------------------------------------------------------------------------

/** chat_id → user_id */
const chatToUser = new Map(chats.map((c) => [c.id, c.user_id]));

/** message_id → user_id  (via chat) */
const messageToUser = new Map(
  messages
    .map((m) => [m.id, chatToUser.get(m.chat_id)])
    .filter(([, u]) => u != null),
);

// ---------------------------------------------------------------------------
// Group events by (user_id, day)
// ---------------------------------------------------------------------------

/** Returns a UTC date string "YYYY-MM-DD" from an ISO timestamp */
function toDay(isoString) {
  return isoString.slice(0, 10);
}

/** Map key: "<user_id>|<day>" → { userId, day, createdNotes, resolvedNotes } */
const snapshots = new Map();

function getOrCreate(userId, day) {
  const key = `${userId}|${day}`;
  if (!snapshots.has(key)) {
    snapshots.set(key, { userId, day, createdNotes: 0, resolvedNotes: 0 });
  }
  return snapshots.get(key);
}

// Count created notes (every message is a "note")
for (const msg of messages) {
  const userId = chatToUser.get(msg.chat_id);
  if (!userId) continue;
  const day = toDay(msg.created_at);
  getOrCreate(userId, day).createdNotes += 1;
}

// Count resolved notes
for (const res of resolutions) {
  const userId = messageToUser.get(res.message_id);
  if (!userId) {
    process.stderr.write(
      `Warning: resolution ${res.id} references unknown message ${res.message_id}\n`,
    );
    continue;
  }
  const day = toDay(res.created_at);
  getOrCreate(userId, day).resolvedNotes += 1;
}

// ---------------------------------------------------------------------------
// Generate SQL
// ---------------------------------------------------------------------------
const rows = [...snapshots.values()].sort(
  (a, b) => a.userId.localeCompare(b.userId) || a.day.localeCompare(b.day),
);

const lines = [
  '-- Auto-generated by scripts/message-burndown-snapshots/generate-burndown-snapshots.js',
  '-- Inserts into message_burndown_snapshots; skips duplicates via ON CONFLICT.',
  '',
  'INSERT INTO message_burndown_snapshots (id, day, user_id, created_notes, resolved_notes)',
  'VALUES',
];

const valueLines = rows.map((row, idx) => {
  const id = randomUUID();
  const comma = idx < rows.length - 1 ? ',' : '';
  return `  ('${id}', '${row.day}', '${row.userId}', ${row.createdNotes}, ${row.resolvedNotes})${comma}`;
});

lines.push(...valueLines);
lines.push('ON CONFLICT (user_id, day) DO UPDATE SET');
lines.push('  created_notes  = EXCLUDED.created_notes,');
lines.push('  resolved_notes = EXCLUDED.resolved_notes;');
lines.push('');

const sql = lines.join('\n');

// ---------------------------------------------------------------------------
// Write SQL to file
// ---------------------------------------------------------------------------
const outFile =
  get('--output') ??
  path.join(path.dirname(__filename), 'burndown-snapshots.sql');
const outPath = path.resolve(outFile);
fs.writeFileSync(outPath, sql, 'utf8');

// ---------------------------------------------------------------------------
// Summary
// ---------------------------------------------------------------------------
process.stderr.write(
  `\nGenerated ${rows.length} snapshot row(s) across ${new Set(rows.map((r) => r.userId)).size} user(s).\nSQL written to ${outPath}\n`,
);
